  function applyMeta(dict, lang) {
    const titleKey = document.body && document.body.dataset.metaTitle;
    const descKey = document.body && document.body.dataset.metaDesc;
    const title = dict[titleKey] || dict['meta-title'] || dict['meta-title-login'] || dict['meta-title-compte'] || dict['meta-title-cart'];
    const desc = dict[descKey] || dict['meta-desc'] || dict['meta-desc-login'] || dict['meta-desc-compte'] || dict['meta-desc-cart'];
    if (title) document.title = title;
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && desc) metaDesc.setAttribute('content', desc);
    const ogTitle = document.querySelector('meta[property="og:title"]');
    const ogDesc = document.querySelector('meta[property="og:description"]');
    const twTitle = document.querySelector('meta[name="twitter:title"]');
    const twDesc = document.querySelector('meta[name="twitter:description"]');
    if (ogTitle && title) ogTitle.setAttribute('content', title);
    if (ogDesc && desc) ogDesc.setAttribute('content', desc);
    if (twTitle && title) twTitle.setAttribute('content', title);
    if (twDesc && desc) twDesc.setAttribute('content', desc);
    const ogLocale = document.querySelector('meta[property="og:locale"]');
    if (ogLocale) {
      ogLocale.setAttribute('content', lang === 'fr' ? 'fr_FR' : lang === 'en' ? 'en_US' : 'ar_SA');
    }
  }

