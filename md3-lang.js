  function applyLang(lang, opts) {
    const dict = getMergedDict(lang);
    if (!dict || !T[lang]) return;

    const persist = !opts || opts.persist !== false;
    currentLang = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    if (document.body) {
      document.body.classList.toggle('lang-ar', lang === 'ar');
    }

    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key] !== undefined) el.setAttribute('placeholder', dict[key]);
    });

    document.querySelectorAll('[data-i18n-title]').forEach((el) => {
      const key = el.getAttribute('data-i18n-title');
      if (dict[key] !== undefined) el.setAttribute('title', dict[key]);
    });

    document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
      const key = el.getAttribute('data-i18n-aria');
      if (dict[key] !== undefined) {
        el.setAttribute('aria-label', dict[key]);
        el.setAttribute('title', dict[key]);
      }
    });

    document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
      const key = el.getAttribute('data-i18n-alt');
      if (dict[key] !== undefined) el.setAttribute('alt', dict[key]);
    });

    const banner = document.querySelector('.banner[data-i18n], .banner [data-i18n="banner-client"]');
    if (banner && dict['banner-client']) {
      if (banner.getAttribute('data-i18n') === 'banner-client') {
        banner.textContent = dict['banner-client'];
      }
    }
    const bannerEl = document.querySelector('.banner:not([data-i18n])');
    if (bannerEl && !bannerEl.querySelector('[data-i18n]') && dict['banner-client']) {
      bannerEl.textContent = dict['banner-client'];
    }

    applyMeta(dict, lang);
    applySelectOptions(lang);

    document.querySelectorAll('.lang-btn').forEach((btn) => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    if (persist) {
      try {
        localStorage.setItem(LANG_STORAGE_KEY, lang);
      } catch (_) {}
    }

    if ((!opts || opts.silent !== true) && typeof onChangeCb === 'function') {
      onChangeCb(lang);
    }

    if ((!opts || opts.silent !== true) && typeof window !== 'undefined') {
      window.dispatchEvent(new CustomEvent('md3-lang-changed', { detail: { lang } }));
    }
  }

